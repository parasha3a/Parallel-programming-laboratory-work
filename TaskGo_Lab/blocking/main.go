package main

import (
	"fmt"
	"runtime"
	"sync"
	"time"
)

func main() {
	runtime.GOMAXPROCS(runtime.NumCPU())

	// ---------- 1) Неблокирующая попытка чтения из пустого канала ----------
	ch := make(chan int) // небуферизованный канал int
	select {
	case v := <-ch:
		// Выполнится только если в ch уже есть значение (здесь ветка недостижима при старте).
		fmt.Println("получено сразу:", v)
	default:
		// Ветка default: ни одна case не готова — не ждём, сразу идём дальше (неблокирующее поведение).
		fmt.Println("неблокирующий select (default): канал пуст")
	}

	// ---------- 2) Классическое блокирующее чтение ----------
	single := make(chan int)
	go func() {
		// Имитация работы перед отправкой: получатель в main заблокируется на <-single до этого момента.
		time.Sleep(150 * time.Millisecond)
		single <- 42 // отправка разблокирует ожидающий приём в main
	}()
	// Оператор <-single блокирует текущую горутину (main), пока кто-то не отправит значение.
	fmt.Println("блокирующее чтение single:", <-single)

	// ---------- 3) Несколько горутин пишут в буфер; main читает после завершения всех отправителей ----------
	const senders = 3
	// Ёмкость буфера = senders: отправители могут положить все значения без блокировки на main,
	// пока тот занят; закрытие канала после wg.Wait() сигнализирует for range, что данных больше не будет.
	buf := make(chan int, senders)
	var wg sync.WaitGroup
	for s := 0; s < senders; s++ {
		wg.Add(1)
		go func(id int) {
			defer wg.Done()
			// Разная задержка — порядок прихода значений не фиксирован (конкуренция).
			time.Sleep(time.Duration(30*(id+1)) * time.Millisecond)
			buf <- 10*(id+1) + 2
		}(s)
	}
	// Отдельная горутина ждёт завершения всех отправителей и закрывает канал — иначе for range на buf зависнет.
	go func() {
		wg.Wait()
		close(buf) // после close приёмник получает оставшиеся значения, затем выходит из range
	}()

	fmt.Println("чтение из буферизованного канала после параллельной отправки:")
	for v := range buf {
		fmt.Println("  ", v)
	}
}
