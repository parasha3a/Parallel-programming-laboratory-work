package main

import (
	"fmt"
	"runtime"
	"sync"
)

func main() {
	// Число одновременных «воркеров» берём по числу логических CPU — типичный выбор для CPU-bound задач.
	workers := runtime.NumCPU()
	// GOMAXPROCS ограничивает, сколько потоков ОС может одновременно выполнять код Go
	// (горутин может быть больше; планировщик Go распределяет их по этим потокам).
	runtime.GOMAXPROCS(workers)

	// WaitGroup хранит счётчик незавершённых горутин: Add(+1) при старте задачи, Done() при выходе.
	var wg sync.WaitGroup

	for w := 0; w < workers; w++ {
		wg.Add(1) // зарегистрировали ещё одну горутину до её запуска (иначе возможна гонка с Wait)

		// go — запуск функции в новой горутине (легковесный «поток» под управлением рантайма).
		// id передаём аргументом: иначе цикл мог бы захватить одно и то же изменяющееся w для всех замыканий.
		go func(id int) {
			defer wg.Done() // при любом выходе из функции уменьшить счётчик (пара с Add)
			fmt.Printf("Hello from горутины %d из %d\n", id+1, workers)
		}(w)
	}

	// Блокируем main до тех пор, пока счётчик WaitGroup не станет нулём — все горутины завершились.
	wg.Wait()
	fmt.Println("main: все горутины завершены (sync.WaitGroup)")
}
